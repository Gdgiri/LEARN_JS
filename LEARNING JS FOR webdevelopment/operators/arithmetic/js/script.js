let num1=5;
let num2=20;

// arithmetic addition(+)
console.log(num1+num2);

// arithmetic subtraction(-)
console.log(num1-num2);

// arithmetic multiplication(*)
console.log(num1*num2);

// arithmetic division(/)
console.log(num1/num2);

// arithmetic module orreminder of division(%)
console.log(num1%num2);

// arithmetic increment(++)
console.log(num1++);
console.log(++num1);
console.log(num1);

// arithmetic decrement(--)
console.log(num1--);
console.log(--num1);
console.log(num1);

