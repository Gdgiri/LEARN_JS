// comparision operator


// comparision operator produces boolean (true or false) as a result!

console.log(1<5);//less then symbol "<"

console.log(1>5);// greater then symbol ">"

// relational operator

let x=5;

console.log(x<1);
console.log(x>1);
console.log(x<=1);
console.log(x>=1);
// equaltity operator

console.log(x===1);
console.log(x!==1);

//string comparision

// in string type it compare the value into dictionary

console.log('hari'<'giri');
console.log('hari'>'giri');

//comparision of different types

// in inside of string '' what we gave it works that type automatically

console.log('1'>5);//1<5
console.log('1'<5);//1>5

// equality operator

// strict equality operator 

console.log(1===1); // Number===Number [true]

// in this strict equality operator as both A and B operand as same datatype and same value if it diifferent it won't work.

console.log('1'===1); // String===Number [false]

// lose equality operator

console.log(1==1); // Number==Number is [true]

// in this lose equality operator as operand of A as different datatype and B operand as  same value.

console.log('1'==1); // String==Number is [true]

