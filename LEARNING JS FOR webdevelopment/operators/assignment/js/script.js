let num1=5;


// arithmetic addition(+)
num1+=2
console.log(num1);

num1+=5
console.log(num1);


// arithmetic subtraction(-)
num1-=2
console.log(num1);


// arithmetic multiplication(*)
num1*=2
console.log(num1);


// arithmetic division(/)
num1/=2
console.log(num1);


// arithmetic module orreminder of division(%)
num1%=2
console.log(num1);

